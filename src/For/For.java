package For;

public class For {
    public static void main(String[] args) {

        for (int i = 15; i <= 40; i = i + 1){
            System.out.println(i);

            if (i == 16){
                System.out.println("car education");
            }
            if (i == 18){
                System.out.println("Take prava car");
            }
            if (i == 35){
                System.out.println("Prezident");
            }
        }
    }
}
